    private Empresa crearEmpresa(){
        Empresa miEmpresa = null;
        String nombreEmpresa;     // Nombre de la Empresa.
        String NITdeLaEmpresa;     // NIT de la Empresa.
        
        nombreEmpresa = this.obtenerTexto(jTextField_Empresa);
        if( nombreEmpresa.equals("") ) return null;
        
        NITdeLaEmpresa = this.obtenerTexto(jTextField_NIT);
        if( NITdeLaEmpresa.equals("") ) return null;
        
        miEmpresa = new Empresa( nombreEmpresa, NITdeLaEmpresa );
        return miEmpresa;
    }   // crearCurso()  
    
    private Trabajador crearTrabajador() {
        Trabajador unTrab = null;
        // Variables locales:        
        String cedula;          // Cédula del trabajador.
        String apeNom;          // Apellidos y Nombres del trabajador.
        int antLab;             // Antiguedad laboral.
        double smn;             // Salario mensual nominal.
             
        cedula = this.obtenerTexto(jTextField_Cedula ).toUpperCase() ;
        if( cedula.equals("") ) return null;
        
        apeNom = this.obtenerTexto( jTextField_ApeNom ).toUpperCase() ;
        if( apeNom.equals("") ) return null;
        
        String strAntLab = this.obtenerTexto(jTextField_antLab );
        if( strAntLab.equals("") ) return null;
        
        String strSMN = this.obtenerTexto(jTextField_SMN );
        if( strSMN.equals("") ) return null;
             
        // Las siguientes lecturas pueden generar una NumberFormatException; 
        // por tanto, se realizan con un try-catch:
        try {
            antLab = Integer.parseInt( strAntLab ) ;
            if( antLab < 0 ) return null;
            smn = Double.parseDouble( strSMN ) ;
            if( smn <= 0 ) return null;            
        }   // try
        catch( NumberFormatException  nfe ) {
            JOptionPane.showMessageDialog( null, nfe, 
                "Number Format Exception", 0 ) ;
            System.out.println( nfe ) ;
            return null;    // Salga del método con el valor null
        }   // catch        
        unTrab = new  Trabajador(cedula, apeNom, antLab, smn); 
        return unTrab;         
    }   // NO BORRAR: FIN DEL MÉTODO crearTrabajador( )
    
    
