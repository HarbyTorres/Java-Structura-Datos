    private void jB_SMN_AltosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_SMN_AltosActionPerformed
        // TODO add your handling code here:
        if( empresa == null || ! empresa.getArreglosCreados() ) return ;
        double smnBase;
        try {
            do {
                smnBase = Double.parseDouble( JOptionPane.showInputDialog(
                    "Digite el SMN (Salario Mensual Nominal) BASE: $ ") ) ;
                if(smnBase < Trabajador.SMMLV) {
                    JOptionPane.showMessageDialog(null, smnBase + " está por " + 
                        "debajo del SMMLV", "Error SMN Base", 0);
                }   // if
            } while(smnBase < Trabajador.SMMLV);
        }
        catch(NumberFormatException nfe) {
            System.out.println(nfe);
            return; // Salga del método.
        }                
        jTextAreaReporte.setText(empresa.obtenerAltosSalarios(smnBase));            
    }//GEN-LAST:event_jB_SMN_AltosActionPerformed

